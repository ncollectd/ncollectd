static int http_test_get(char *path)
{
    http_header_t headers[] = {
        { .header_name = HTTP_HEADER_HOST, .value = "localhost" },
    };
    http_request_t request = {
        .method = HTTP_METHOD_GET,
        .path = path,
        .version = HTTP_VERSION_1_1,
        .headers.num = STATIC_ARRAY_SIZE(headers),
        .headers.ptr = headers,
        .content_length = 0,
        .content = NULL
    };
    http_response_t response = {0};
    int timeout = 1000;

    current_http_request = &request;
    int fd = socket_connect_tcp("127.0.0.1", 32222, 0, 0);
    if (fd < 0) {
        return -1;
    }

    int status = http_fetch(fd, &request, &response, &timeout);
    if (status != 0) {
        http_response_reset(&response);
        close(fd);
        return -1;
    }

    http_response_reset(&response);
    close(fd);

    if (response.status_code != HTTP_STATUS_200) {
        return -1;
    }

    return 0;
}

